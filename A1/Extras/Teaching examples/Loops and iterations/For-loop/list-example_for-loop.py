# EN : We have a list variable containing a couple of lines of text
# NL : We hebben een lijst variable die een paar linen aan text bevatten
list_data = ["Never gonna give you up", "Never gonna let you down", "Never gonna run around and desert you", "Never gonna make you cry", "Never gonna say goodbye", "Never gonna tell a lie and hurt you"]

# EN : With the help of a for loop we can get each object in the list separately
# NL : Met behulp van een For-loop kunnen we elk onderdeel uit de lijst.
for i in list_data:
    print(i)